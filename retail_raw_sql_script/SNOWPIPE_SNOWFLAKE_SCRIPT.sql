/*4. Sales and Revenue Analysis: 
o Analyze transaction data to understand sales patterns, quantities, and 
sales values. */

SELECT 
    P.DEPARTMENT,
    SUM(T.QUANTITY) AS TOTAL_UNIT_SOLD,
    SUM(T.SALES_VALUE) AS TOTAL_REVENUE,
    ROUND(SUM(T.SALES_VALUE) / NULLIF(SUM(T.QUANTITY), 0), 2) AS AVG_UNIT_PRICE
FROM RETAILS.PUBLIC.TRANSACTION_NEW T
JOIN RETAILS.PUBLIC.PRODUCT_RAW P ON T.PRODUCT_ID = P.PRODUCT_ID
GROUP BY 1
ORDER BY Total_Revenue DESC;

/*o Identify the top-selling products, brands, and departments. */

WITH BRAND_SALES AS (
    SELECT 
        P.DEPARTMENT,
        P.BRAND,
        SUM(T.SALES_VALUE) AS BRAND_REVENUE,
        RANK() OVER(PARTITION BY P.DEPARTMENT ORDER BY SUM(T.SALES_VALUE) DESC) as SALES_BANK
    FROM RETAILS.PUBLIC.TRANSACTION_NEW T
    JOIN RETAILS.PUBLIC.PRODUCT_RAW P ON T.PRODUCT_ID = P.PRODUCT_ID
    GROUP BY 1, 2
)
SELECT * FROM BRAND_SALES 
WHERE SALES_BANK <= 5;     --- TOP 5 BRANDS IN EVERRY DEPARTMENT


/*o Explore the correlation between sales and discounts (retail discount, 
coupon discount, coupon match discount).*/

SELECT 
    P.COMMODITY_DESC,
    -- Calculate Total Discount across all types
    SUM(ABS(T.RETAIL_DISC) + ABS(T.COUPON_DISC) + ABS(T.COUPON_MATCH_DISC)) AS Total_Discount_Applied,
    SUM(T.SALES_VALUE) AS Total_Sales_Value,
    -- Measure the "Depth" of the discount
    ROUND(
        SUM(ABS(T.RETAIL_DISC) + ABS(T.COUPON_DISC) + ABS(T.COUPON_MATCH_DISC)) * 100.0 / 
        NULLIF(SUM(T.SALES_VALUE + ABS(T.RETAIL_DISC) + ABS(T.COUPON_DISC) + ABS(T.COUPON_MATCH_DISC)), 0), 
    2) AS Discount_Depth_Pct,
    -- Measure if depth leads to more units per basket
    ROUND(SUM(T.QUANTITY) * 1.0 / COUNT(DISTINCT T.BASKET_ID), 2) AS Avg_Units_Per_Basket
FROM RETAILS.PUBLIC.TRANSACTION_NEW T
JOIN RETAILS.PUBLIC.PRODUCT_RAW P ON T.PRODUCT_ID = P.PRODUCT_ID
GROUP BY 1
HAVING Total_Sales_Value > 100 -- Focus on products with meaningful data
ORDER BY Discount_Depth_Pct DESC;


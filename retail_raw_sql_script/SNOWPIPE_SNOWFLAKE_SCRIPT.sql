/*1. Customer Demographics KPIs: 
A) Count of unique households: Measure the total number of unique households in 
the Demographic table.*/

SELECT COUNT(DISTINCT HOUSEHOLD_KEY) AS TOTAL_HOUSEHOLDS FROM RETAILS.PUBLIC.DEMOGRAPHIC_RAW;

/* B) Household composition distribution: Analyze the distribution of household 
compositions (HH_COMP_DESC) to understand the composition of households. */

SELECT HH_COMP_DESC, COUNT(DISTINCT HOUSEHOLD_KEY) AS TOTAL_HOUSEHOLDS
FROM RETAILS.PUBLIC.DEMOGRAPHIC_RAW
GROUP BY 1
ORDER BY 2 DESC;

/*C) Age distribution: Calculate the percentage or count of customers in different age 
groups (AGE_DESC).*/

SELECT AGE_DESC,TOTAL_HOUSEHOLDS,ROUND(TOTAL_HOUSEHOLDS/2500 * 100,2) AS PERC_AGEWISE_HOUSEHOLDS_DIST
FROM (
SELECT AGE_DESC, COUNT(DISTINCT HOUSEHOLD_KEY) AS TOTAL_HOUSEHOLDS
FROM RETAILS.PUBLIC.DEMOGRAPHIC_RAW
GROUP BY 1
ORDER BY 2 DESC
)
GROUP BY 1,2;

/*D) Marital status distribution: Analyze the proportion of customers in different 
marital status categories (MARITAL_STATUS_CODE). */

SELECT MARITAL_STATUS_CODE, COUNT(DISTINCT HOUSEHOLD_KEY) AS TOTAL_HOUSEHOLDS,
ROUND(COUNT(DISTINCT HOUSEHOLD_KEY)/2500 * 100,2) AS PERC_MARITAL_HOUSEHOLD_DIST
FROM RETAILS.PUBLIC.DEMOGRAPHIC_RAW
GROUP BY 1
ORDER BY 2 DESC;


/*E) Income distribution: Determine the distribution of customers across income levels 
(INCOME_DESC).*/

SELECT INCOME_DESC,COUNT(DISTINCT HOUSEHOLD_KEY) AS TOTAL_HOUSEHOLDS,
ROUND(COUNT(DISTINCT HOUSEHOLD_KEY)/2500 * 100,2)
FROM RETAILS.PUBLIC.DEMOGRAPHIC_RAW
GROUP BY 1
ORDER BY 2;


/*F) Homeownership distribution: Calculate the percentage or count of customers who 
own or rent their homes (HOMEOWNER_DESC).*/

SELECT HOMEOWNER_DESC , 
COUNT(DISTINCT HOUSEHOLD_KEY) AS TOTAL_HOUSEHOLDS,
ROUND(COUNT(DISTINCT HOUSEHOLD_KEY) / 2500 * 100 , 2) AS PERC_HOMEOWNER_DESC_DISTR
FROM RETAILS.PUBLIC.DEMOGRAPHIC_RAW
GROUP BY 1
ORDER BY 2 DESC;


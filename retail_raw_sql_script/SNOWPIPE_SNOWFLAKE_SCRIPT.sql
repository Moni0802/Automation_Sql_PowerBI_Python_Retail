CREATE OR REPLACE PROCEDURE DISCOUNT_IMPACT_ON_SALES_KPI()
RETURNS STRING
LANGUAGE SQL AS
$$
CREATE OR REPLACE TABLE DISCOUNT_IMPACT_ON_SALES_KPI AS
(SELECT 
    P.DEPARTMENT,
    SUM(T.SALES_VALUE) AS Net_Revenue,
    SUM(ABS(T.RETAIL_DISC)) AS Total_Retail_Disc,
    SUM(ABS(T.COUPON_DISC) + ABS(T.COUPON_MATCH_DISC)) AS Total_Coupon_Disc,
    SUM(T.SALES_VALUE + ABS(T.RETAIL_DISC) + ABS(T.COUPON_DISC) + ABS(T.COUPON_MATCH_DISC)) AS Gross_Revenue,
    ROUND(
        (SUM(ABS(T.RETAIL_DISC) + ABS(T.COUPON_DISC) + ABS(T.COUPON_MATCH_DISC)) * 100.0) / 
        NULLIF(SUM(T.SALES_VALUE + ABS(T.RETAIL_DISC) + ABS(T.COUPON_DISC) + ABS(T.COUPON_MATCH_DISC)), 0), 
    2) AS Discount_Impact_Pct
FROM RETAILS.PUBLIC.TRANSACTION_NEW T
JOIN RETAILS.PUBLIC.PRODUCT_RAW P ON T.PRODUCT_ID = P.PRODUCT_ID
GROUP BY 1
ORDER BY Discount_Impact_Pct DESC);
$$;

CALL DISCOUNT_IMPACT_ON_SALES_KPI();


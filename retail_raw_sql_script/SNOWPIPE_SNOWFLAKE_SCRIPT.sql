/*2. Customer Segmentation: 
o A) Segment customers based on demographic attributes such as age, 
marital status, income, and household composition. */

SELECT AGE_DESC,INCOME_DESC,HH_COMP_DESC,
COUNT(HOUSEHOLD_KEY) AS SEGMENT_SIZE,
    CASE 
        WHEN INCOME_DESC IN ('100-124K', '125-149K', '150-174K', '175-199K', '200-249K', '250K+') THEN 'High Earners'
        WHEN HH_COMP_DESC = '2 Adults, No Kids' THEN 'Empty Nesters / DINKs'
        WHEN HH_COMP_DESC = '2 Adults, Kids' THEN 'Traditional Families'
        ELSE 'Other'
    END AS SEGMENT_LABEL
FROM RETAILS.PUBLIC.DEMOGRAPHIC_RAW
GROUP BY 1, 2, 3
ORDER BY SEGMENT_SIZE DESC;

/*o B) Analyze customer behavior and preferences using transaction data.*/

SELECT T.HOUSEHOLD_KEY,
    COUNT(DISTINCT T.BASKET_ID) AS FREQUENCY,
    SUM(T.SALES_VALUE) AS TOTAL_REVENUE,
    ROUND(SUM(T.SALES_VALUE) / COUNT(DISTINCT T.BASKET_ID), 2) AS AVG_BASKET_VALUE,
    CASE 
        WHEN COUNT(DISTINCT T.BASKET_ID) > 50 THEN 'Loyalist'
        WHEN COUNT(DISTINCT T.BASKET_ID) BETWEEN 10 AND 50 THEN 'Regular'
        ELSE 'Occasional'
    END AS LOYALTY_TIE
FROM RETAILS.PUBLIC.TRANSACTION_NEW T
GROUP BY T.HOUSEHOLD_KEY;

/*o C) Group customers based on their response to campaigns and coupon 
redemption. */

SELECT 
    T.household_key,
    SUM(ABS(T.COUPON_DISC)) AS Total_Coupon_Savings,
    COUNT(DISTINCT CRN.COUPON_UPC) AS Unique_Coupons_Redeemed,
    CASE 
        WHEN SUM(ABS(T.COUPON_DISC)) > 50 THEN 'Coupon Hunter'
        WHEN SUM(ABS(T.COUPON_DISC)) > 0 THEN 'Casual Saver'
        ELSE 'Full Price Buyer'
    END AS Savings_Profile
FROM RETAILS.PUBLIC.TRANSACTION_NEW T
LEFT JOIN RETAILS.PUBLIC.COUPON_REDEMPT_NEW CRN ON T.household_key = CRN.household_key
GROUP BY T.household_key;

/*3. Coupon KPIs: 
o A) Coupon redemption rate: Calculate the percentage of coupons redeemed (from the 
coupon_redempt table) compared to the total number of coupons distributed (from 
the Coupon table). */
CREATE OR REPLACE PROCEDURE PCT_COUPON_REDEEM_KPI()
RETURNS STRING
LANGUAGE SQL AS
$$
CREATE OR REPLACE TABLE PCT_COUPON_REDEEM_KPI AS (SELECT 
    CR.CAMPAIGN,
    COUNT(DISTINCT CRN.COUPON_UPC) AS Unique_Redeemed,
    COUNT(DISTINCT CR.COUPON_UPC) AS Total_Issued,
    ROUND(
        (COUNT(DISTINCT CRN.COUPON_UPC) * 100.0) / 
        (COUNT(DISTINCT CR.COUPON_UPC)), 
    2) AS REDEMPTION_RATE_PCT
FROM RETAILS.PUBLIC.COUPON_RAW CR
LEFT JOIN RETAILS.PUBLIC.COUPON_REDEMPT_NEW CRN 
    ON CR.CAMPAIGN = CRN.CAMPAIGN
GROUP BY CR.CAMPAIGN
ORDER BY REDEMPTION_RATE_PCT DESC);
$$;
CALL PCT_COUPON_REDEEM_KPI();
